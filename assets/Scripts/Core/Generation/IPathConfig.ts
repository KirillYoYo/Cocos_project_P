/**
 * Параметры процедурной генерации пути (safe corridor).
 * Передаётся из конфига уровня в PathGenerator.
 */
export interface IPathConfig {
    /** Частота волны Perlin noise (0.01 = плавная, 0.05 = резкая) */
    noiseFrequency: number;

    /** Амплитуда отклонения центра коридора от середины экрана (px) */
    noiseAmplitude: number;

    /** Начальная ширина безопасного коридора (px) */
    corridorWidth: number;

    /** Минимальная ширина коридора после сужения (px) */
    corridorWidthMin: number;

    /** Скорость сужения коридора (px на единицу пройденного расстояния) */
    corridorShrinkRate: number;

    /** Seed для воспроизводимости генерации (опционально) */
    seed?: number;

    /** Длина одного сегмента пути (px) — шаг дискретизации кривой */
    segmentLength: number;

    /** Сколько сегментов генерировать заранее за правым краем экрана */
    prebuildSegments: number;
}

/**
 * Один сегмент сгенерированного пути.
 * Описывает «срез» коридора в конкретной X-позиции.
 */
export interface PathSegment {
    /** X-координата сегмента в мировом пространстве */
    x: number;

    /** Y-координата центра коридора */
    centerY: number;

    /** Ширина коридора в этой точке (может сужаться) */
    corridorWidth: number;
}

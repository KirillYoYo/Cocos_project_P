/**
 * Параметры физики корабля (Box2D).
 * Передаётся из конфига уровня в PlaneController.
 */
export interface IPlaneConfig {
    /** Горизонтальная скорость корабля (px/сек) — определяет скорость скролла мира */
    forwardSpeed: number;

    /**
     * Множитель гравитации для RigidBody2D.
     * 0 = нет гравитации, 1 = полная гравитация мира.
     * Рекомендуется 0.3–0.8 — корабль постепенно падает без ввода.
     */
    gravityScale: number;

    /** Сила, прикладываемая вверх при нажатии (N). Должна превышать гравитацию для подъёма */
    upForce: number;

    /** Дополнительная сила вниз при нажатии (N). Суммируется с гравитацией → быстрое пикирование */
    downForce: number;

    /** Максимальная скорость вверх (px/сек) */
    maxUpSpeed: number;

    /** Максимальная скорость вниз (px/сек) */
    maxDownSpeed: number;

    /**
     * Линейное затухание (linearDamping) — сопротивление воздуха.
     * 0 = нет затухания, 2–5 = ощутимое торможение.
     * Box2D применяет каждый шаг: velocity *= 1 / (1 + dt * damping)
     */
    linearDamping: number;

    /** Радиус CircleCollider2D корабля (px) */
    colliderRadius: number;
}

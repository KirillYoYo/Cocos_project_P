/**
 * Параметры физики корабля.
 * Передаётся из конфига уровня в PlaneSystem.
 */
export interface IPlaneConfig {
    /** Горизонтальная скорость корабля (px/сек) — определяет скорость скролла мира */
    forwardSpeed: number;

    /** Ускорение при движении вверх (px/сек²) — медленное, тяжёлый подъём */
    upAcceleration: number;

    /** Ускорение при движении вниз (px/сек²) — быстрое, «пикирование» */
    downAcceleration: number;

    /** Максимальная скорость вверх (px/сек) */
    maxUpSpeed: number;

    /** Максимальная скорость вниз (px/сек) */
    maxDownSpeed: number;

    /**
     * Коэффициент затухания инерции (0–1).
     * 0 = нет затухания (бесконечный полёт),
     * 0.95 = быстрое торможение,
     * 0.5 = медленное торможение.
     * Формула: velocityY *= (1 - damping) ^ dt
     */
    inertiaDamping: number;
}
